package textcrypto.View.Controller;

import java.net.URL;
import java.util.ResourceBundle;
import javafx.fxml.FXML;
import javafx.fxml.Initializable;
import javafx.scene.image.Image;
import javafx.scene.image.ImageView;

/**
 * FXML Controller class
 *
 * @author Rafael S. Müller <rafael.mueller1@gmail.com>
 */
public class ControllerAbout implements Initializable {

    @FXML
    private ImageView imgViewlogo;

    /**
     * Initializes the controller class.
     *
     * @param url
     * @param rb
     */
    @Override
    public void initialize(URL url, ResourceBundle rb) {
        Image img = new Image(getClass().getClassLoader().
                getResourceAsStream("res/images/logo200.png"));
        imgViewlogo.setImage(img);
    }
}
